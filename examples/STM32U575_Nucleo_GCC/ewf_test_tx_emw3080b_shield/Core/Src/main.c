/* USER CODE BEGIN 0 */
void thread_sample_entry(ULONG thread_input)
{
    ewf_result result;

    ewf_allocator* message_allocator_ptr = NULL;
    ewf_interface* interface_ptr = NULL;
    ewf_adapter* adapter_ptr = NULL;

    EWF_ALLOCATOR_THREADX_STATIC_DECLARE(message_allocator_ptr, message_allocator, EWF_CONFIG_MESSAGE_ALLOCATOR_BLOCK_COUNT, EWF_CONFIG_MESSAGE_ALLOCATOR_BLOCK_SIZE);
    EWF_INTERFACE_STM32_UART_STATIC_DECLARE(interface_ptr, stm32_uart_port, &hlpuart1);
    EWF_ADAPTER_MXCHIP_EMW3080B_STATIC_DECLARE(adapter_ptr, emw3080b, message_allocator_ptr, NULL, interface_ptr);

    /* Start the adapter.  */
    if (ewf_result_failed(result = ewf_adapter_start(adapter_ptr)))
    {
        EWF_LOG_ERROR("Failed to start the adapter, ewf_result %d.\n", result);
        exit(result);
    }

    /* Connect the adapter to a WiFi network */
    if (ewf_result_failed(result = ewf_adapter_wifi_station_connect(adapter_ptr, EWF_CONFIG_WIFI_STATION_SSID, EWF_CONFIG_WIFI_STATION_PASSWORD)))
    {
        EWF_LOG_ERROR("Failed to connect to a WiFi network, ewf_result %d.\n", result);
        return;
    }

    /* Run the adapter test */
    if (ewf_result_failed(result = ewf_adapter_mxchip_emw3080b_test(adapter_ptr)))
    {
        EWF_LOG_ERROR("The test function returned an error, ewf_result %d.\n", result);
        return;
    }

    /* Disconnect the adapter */
    if (ewf_result_failed(result = ewf_adapter_wifi_station_disconnect(adapter_ptr)))
    {
        EWF_LOG_ERROR("Failed to disconnect the adapter from the WiFi network, ewf_result %d.\n", result);
        return;
    }

    EWF_LOG("\nDone!\n");

    /* Stay here forever.  */
    while (1)
    {
        EWF_LOG(".");
        ewf_platform_sleep(EWF_PLATFORM_TICKS_PER_SECOND);
    }
}

